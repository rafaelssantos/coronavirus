		<h1>Unesp/Prudente participa do 3º Seminário de avaliação dos INCTs</h1>
		<h5>Realizado no período de 18 a 20 de novembro deste ano em Brasília-DF</h5>
		
		<p>INCT (Instituto Nacional de Ciência e Tecnologia) é um dos maiores programas de pesquisa que vem sendo desenvolvido no Brasil e que conta com financiamento do CNPq, CAPES e das fundações de pesquisas dos estados, além de outros órgãos com algum interesse específico num tema. De 18 a 20 de novembro deste ano foi realizado em Brasília o 3º. Seminário de Avaliação dos INCTs, onde ocorreu a avaliação dos 105 institutos existentes no Brasil.</p>
		<p>O INCT Tecnologia GNSS no suporte à Navegação Aérea (GNSS NavAer), que conta com a participação do ITA, IAE, INPE, UFRGS e IFSP-PPE e é coordenado pela Unesp de Presidente Prudente foi avaliado durante o seminário. Na ocasião, o INCT GNSS NavAer foi representado pelo seu coordenador, Prof. Dr. João Francisco Galera Monico, do Departamento de Cartografia, que apresentou os resultados obtidos até o momento e pelo Dr. Bruno Vani, membro do INCT e professor do IFSP da cidade de Presidente Epitácio. Ocorreu durante o evento uma oficina sobre a divulgação científica, uma componente muito importante dos INCTs. </p>
		<p>Foi acentuada a importância da aproximação das instituições de pesquisas com os agentes de comunicação, escrita, falada e media digital. Essa notícia já visa atender esse objetivo. A divulgação visa, de forma geral, mostrar para a sociedade a importância do desenvolvimento da Ciência, Tecnologia e Inovação para o progresso do país.</p>
		
