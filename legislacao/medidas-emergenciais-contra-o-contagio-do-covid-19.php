		<h1>Decreto  Medidas Temporárias e Emergenciais de Prevenção de Contágio pelo COVID-19 ─ DECRETO Nº 4.101</h1>
		<span class='author'>O texto reproduzido tem caráter informativo e não substitui o <a href=""></a>publicado no diário oficial do município</span>

		<p><strong>"DISPÕE SOBRE A ADOÇÃO DE MEDIDAS TEMPORÁRIAS E EMERGENCIAIS DE PREVENÇÃO DE CONTÁGIO PELO COVID-19 (NOVO CORONAVIRUS), NO ÂMBITO DA ADMINISTRAÇÃO PÚBLICA E RECOMENDAÇÕES NO SETOR PRIVADO MUNICIPAL"</strong></p>

		<p><strong>MARCELO DE SOUZA PECCHIO</strong>, Prefeito do Município de Quatá, Estado de São Paulo, no uso e suas atribuições legais;</p>

		<p><strong>CONSIDERANDO</strong> a existência da pandemia do COVID-19 (Novo Coronavírus), nos termos declarados pela Organização Mundial da Saúde;</p>

		<p><strong>CONSIDERANDO</strong> o Decreto do Governador do Estado, João Dória, que tratou da adoção de prevenção de contágio pelo COVID-19 no âmbito Estadual;</p>

		<p><strong>CONSIDERANDO</strong> a necessidade de mitigação de disseminação da doença em face dos elevados riscos de saúde pública;</p>

		<p><strong>DECRETA:</strong></p>

		<p><strong>Artigo 1º</strong> – Que os Secretários Municipais e dirigentes máximos de entidades autárquicas adotarão as providências necessárias em seus respectivos âmbitos visando o seguinte:</p>

		<p>I – suspensão imediata de eventos públicos, incluída a programação de atividades culturais, sociais e esportivas, como cursos e oficinas, aulas de esporte, atividades voltadas à terceira idade, bem como qualquer atividade que possa gerar aglomeração de pessoas;</p>

		<p>II – suspensão imediata de reuniões e treinamentos internos;</p>

		<p>III – suspensão total de aulas no âmbito da Secretaria Municipal da Educação e da rede particular de ensino, a partir do dia 18 de março de 2020, por prazo indeterminado, sem imputação de faltas aos alunos;</p>

		<p>IV – suspensão do gozo de férias e licença prêmio dos servidores da Secretaria Municipal da Saúde, por tempo indeterminado, bem como sua imediata convocação para se apresentarem num prazo máximo de 48 (quarenta e oito) horas, conforme entenda a Secretaria Municipal de Saúde.</p>

		<p>V – suspensão, no período de 18 de março de 2020 à 31/03/2020, do atendimentos ao público em todas as repartições públicas municipais e autárquicas, exceto os serviços essenciais de saúde, segurança, limpeza urbana e transporte público, bem como dos serviços imprescindíveis para a continuidade da gestão em geral.</p>

		<p>VI – servidores, acima de 60 (sessenta) anos, e os que se enquadram no grupo de risco estabelecido pelo Ministério da Saúde, com exceção dos lotados na Secretaria Municipal de Saúde, terão suas atividades presenciais suspensas, podendo ser suas atividades realizadas remotamente com o uso das tecnologias disponíveis (home office).</p>

		<p>§ 1º - o fechamento dos órgãos públicos municipais, por prazo indeterminado, com dispensa de seus servidores, exceto aqueles cujas atividades sejam consideradas essenciais para o funcionamento do serviço municipal, a critério do Secretário Municipal de cada pasta, com ciência ao Comitê Gestor de Crise.</p>


		<p>§ 2º - Mantem-se o regular prosseguimento dos processos licitatórios e processos admissionais, nos termos da legislação vigente.</p>

		<p><strong>Artigo 2º</strong> – No âmbito dos outros poderes, órgãos e entidades autônomas, bem como no setor privado do Município, fica recomendada:</p>

		<p>I – a suspensão de eventos públicos, com aglomeração de pessoas, inclusive em Igrejas, Clubes, academias, feiras livres ou outros estabelecimentos congêneres.</p>

		<p>II – que sejam reforçadas as medidas de higienização e disponibilizados álcool gel 70% em locais de grande circulação de pessoas, como bares, restaurantes e comércio em geral.</p>

		<p><strong>Artigo 3º</strong> – Ficam adiados, por tempo indeterminado, todos os eventos oficiais ou comemorativos no Município.</p>

		<p><strong>Artigo 4º</strong> – Fica criado o Comitê de Gestão de Crise (CGC), de enfrentamento ao COVID-19, que será coordenado pelo Gabinete do Prefeito e contará com a participação de representantes de cada uma das Secretarias Municipais.</p>

		<p>Parágrafo único – O Poder Público Municipal manterá uma Central Única de Atendimento, via telefone e para os casos que apresentem sintomas, pessoalmente na Unidade Ambulatorial “João Jorge Estevam” – Santa Casa.</p>

		<p><strong>Artigo 5º</strong> – As medidas previstas neste Decreto poderão ser reavaliadas a qualquer momento.</p>

		<p><strong>Artigo 6º</strong> - A tramitação dos processos referentes a assuntos vinculados a este Decreto correrá em regime de urgência e prioridade em todos os órgãos e entidades da Administração Pública Municipal.</p>

		<p><strong>Artigo 7º</strong> – Este Decreto entra em vigor a partir de 18 de março de 2020, com suas medidas sendo adotadas por tempo indeterminado, conforme orientações dos órgãos competentes.</p>

