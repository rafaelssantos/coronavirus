        <p><strong>"ALTERA O DECRETO MUNICIPAL Nº. 4.107 DE 26/03/2020, COM REDAÇÃO DADA PELO DECRETO MUNICIPAL N° 4.109 DE 01/04/2020 E PRORROGA O PERÍODO DE QUARENTENA NO MUNICÍPIO DE QUATÁ, NO CONTEXTO DA PANDEMIA DO COVID-19 E DÁ OUTRAS PROVIDÊNCIAS"</strong></p>

        <p><strong>MÁRCIO BIDÓIA</strong>, Prefeito Municipal de Quatá em exercício, no uso de suas atribuições legais;</p>

        <p><strong>CONSIDERANDO</strong> o disposto no Decreto Municipal nº 4.107 de 26 de março de 2020, que estabeleceu  QUARENTENA no Município de Quatá;</p>

        <p><strong>CONSIDERANDO</strong> o disposto no Decreto Estadual nº 64.920 de 06 de abril de 2020, que estendeu a Quarentena no Estado de São Paulo até 22 de abril de 2020;</p>

        <p><strong>CONSIDERANDO</strong> a necessidade de se alinhar as determinações do Governo do Estado de São Paulo com as determinações do Poder Público Municipal;</p>

        <p><strong>DECRETA:</strong>

            <p><strong>Artigo 1º</strong> – Fica estendido o período de quarentena estabelecido no artigo 1º do Decreto Municipal nº 4.107 de 26 de março de 2020, até <strong>22 de abril de 2020</strong>, no Município de Quatá, como medida necessária ao enfrentamento da pandemia da COVID-19 (Novo CORONAVÍRUS).</p>

            <p><strong>Artigo 2º</strong> – Fica recomendado que a circulação de pessoas no âmbito do Município de Quatá se limite às necessidades imediatas de alimentação, cuidados de saúde e exercício de atividades essenciais.</p>

            <p><strong>Artigo 3º</strong> – EO artigo 3º, do Decreto nº. 4.103, de 21 de março de 2020, alterado pelo Decreto nº 4.107/2020, e alterações posteriores, passa a vigorar com a seguinte redação: </p>
            
            <em>
            <p>"Artigo 3º – Em virtude da aglomeração ou circulação de pessoas, no âmbito dos outros poderes, órgãos e entidades autônomas, bem como no setor privado do Município, fica determinada a suspensão, no período de 08/04/2020 a 22/04/2020, de todas as atividades e serviços não essenciais, como academias, clubes, associações recreativas, bem como a realização de missas, cultos e atividades religiosas, reuniões ou eventos com público de qualquer natureza.</p>

                <p><strong>§ 1º</strong> – Fica autorizado o funcionamento do comércio em geral (lojas e afins), incluindo-se bares, lanchonetes, restaurantes, conveniências e demais estabelecimentos de gêneros alimentícios, exclusivamente para atendimento de serviços com entrega a domicílio (delivery), sendo que os estabelecimentos comerciais deverão manter fechadas as portas de acesso do público ao seu interior.</p>

                <p><strong>§ 2º</strong> – Fica permitido o funcionamento do comércio de cunho essencial, como farmácias, mercados, mercearias, supermercados, açougues, padarias e quitandas, ficando recomendado que atenda as recomendações de higienização das mãos com álcool gel, tanto de seus funcionários como dos clientes, evite a aglomeração de pessoas, controlando o número de pessoas no interior do estabelecimento.</p>

                <p><strong>§ 3º</strong> – Fica permitido o funcionamento dos postos de gasolina, nos moldes da determinação expressa no artigo 5º da Resolução da ANP nº. 812, de 23 de março de 2020, ressaltando que as conveniências deverão estar fechadas para o público, somente com a opção de entrega de produtos a domicílio (delivery).</p>

                <p><strong>§ 4º</strong> – Ficam excetuadas da suspensão determinada por este Decreto, o funcionamento das instituições ou agências bancárias, postos de correspondentes bancários e as agências dos correios, por serem consideradas serviço essencial à população e deverão adotar as seguintes providências:</p>

                <p><strong>I</strong>- no atendimento interno deverá ser limitado o número de pessoas e respeitada a distância mínima de 02 (dois) metros entre os pontos de atendimento;</p>

                <p><strong>II</strong>- seja dada preferência ao atendimento nos caixas eletrônicos das agências;</p>
                
                <p><strong>III</strong>- a fila das pessoas que aguardam atendimento deverá ser organizada no lado externo das agências, guardando-se a distância de pelo menos 01 (um) metro entre as pessoas.</p>

                <p><strong>§ 5º</strong> – Os postos de correspondentes bancários, deverão funcionar exclusivamente para o cumprimento do objeto previsto em contrato firmado com a instituição financeira contratante, no horário das 10:00 às 14:00H, vedado o exercício do comércio para produtos ou serviços diversos da atividade bancária autorizada."</p>
            </em>

            <p><strong>Artigo 4º</strong> – O artigo 4º do Decreto nº. 4.103 de 21 de março de 2020, alterado pelo artigo 4º do Decreto nº. 4.107 de 26 de março de 2020, passa a vigorar com a seguinte redação:</p>

            <em>
                <p>"Artigo 4º – Fica autorizado o funcionamento das atividades essenciais como:</p>

                <p>I- serviços de saúde, assistência médica e hospitalar;</p>

                <p>II – clínicas de fisioterapia, consultórios médicos e odontológicos, clínicas de psicologia, laboratórios de análises clínicas e farmacêuticas, limitando o atendimento para os casos extremamente necessários e urgentes;</p>

                <p>III – estabelecimentos comerciais de venda de água e gás, preferencialmente para vendas com entrega a domicílio (delivery);</p>

                <p>IV- agências de concessionária de energia, água e serviços de internet, para atendimentos de urgências;</p>

                <p>V- serviços funerários, com orientações específicas para que os velórios funcionem com capacidade reduzida de pessoas;</p>

                <p>VI- clínicas veterinárias e venda de suprimentos animais;</p>

                <p>VII- lojas de materiais de construção;</p>

                <p>VIII- as oficinas mecânicas e borracharias."</p>
            </em>

            <p><strong>Parágrafo único</strong> – os locais e estabelecimentos mencionados neste artigo deverão adotar as seguintes medidas de segurança:</p>

            <p><strong>I</strong>- disponibilizar álcool gel na entrada dos estabelecimentos para utilização dos funcionários e clientes;</p>

            <p><strong>II</strong>- manter o local de circulação de pessoas arejado;</p>

            <p><strong>III</strong>- limitar o número de pessoas no interior do estabelecimento e evitar aglomeração.</p>



            <p><strong>Artigo 5º</strong> – Ficam ratificados os demais termos do Decreto Municipal nº 4.103 de 21 de março de 2020 e alterações posteriores, sendo que, as medidas previstas poderão ser reavaliadas a qualquer momento.</p>
           
            <p><strong>Artigo 6º</strong> – Este Decreto entra em vigor na data de sua publicação.</p>

