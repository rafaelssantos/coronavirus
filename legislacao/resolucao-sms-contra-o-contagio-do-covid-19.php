        <p><strong>"DISPÕE SOBRE A REGULAMENTAÇÃO DOS DECRETOS Nºs 4.101/20 e 4.103/20 QUE TRATAM DAS MEDIDAS TEMPORÁRIAS E EMERGENCIAIS PARA PREVENÇÃO DO CONTÁGIO PELO COVID-19 (NOVO CORONAVÍRUS), E DO ESTADO DE CALAMIDADE PÚBLICA DECLARADO NO MUNICÍPIO, NO ÂMBITO DA SECRETARIA MUNICIPAL DE SAÚDE DE QUATÁ E DÁ OUTRAS PROVIDÊNCIAS"</strong></p>

        <p><strong>ADRIANA BALEJO PIEDADE DA SILVA</strong>, Secretária Municipal de Saúde, no uso e suas atribuições legais;</p>

        <p><strong>CONSIDERANDO</strong> autorização contida no art. 1º do Decreto nº 4.101/20 do Executivo Municipal que outorgou aos Secretários Municipais a autoridade para a adoção de medidas administrativas, dentro de sua unidade gestora visando a melhor solução para o cumprimento do referido decreto que trata das medidas de prevenção ao Coronavirus, objeto do decreto;</p>

        <p><strong>CONSIDERANDO</strong> as exceções contidas nos incisos V e VI, combinado com o § 2º do art. 1º do Decreto nº 4.101/20, que as medidas de suspensão de atividades e dispensa de pessoal não se aplicam aos servidores e funcionários da Secretaria Municipal de Saúde;</p>

        <p><strong>CONSIDERANDO</strong> a necessidade de se esclarecer aos colaboradores da respectiva Secretaria de Saúde que deverão cumprir com suas atribuições legais do cargo, especialmente neste período de comoção social, onde toda a população quataense necessita do empenho pessoal de todos;</p>

        <p><strong>CONSIDERANDO</strong> que não se sabe a data de outra ocorrência de calamidade pública em saúde do gênero das últimas 3 décadas, e nunca a população precisou tanto da dedicação dos profissionais de saúde de nosso município, e não podemos permitir que posturas isoladas possam prejudicar o trabalho que vem sendo priorizado por todas os colaboradores da prefeitura, e que possa desatender a população;</p>

        <p><strong>CONSIDERANDO FINALMENTE</strong> qque o § 1º do Decreto 4.101/20 deixa claro que qualquer alteração das disposições contidas no decreto ou suspensão dos serviços públicos somente poderão ser feitas com a autorização expressa do secretário da pasta, com a ciência e ouvido o Comitê Gestor da Crise, criado especialmente para esse fim.</p>

        <p><strong>RESOLVE:</strong></p>

        <p><strong>Artigo 1º</strong> – Ficam mantidos todos os serviços de atribuição da Secretaria Municipal de Saúde, serviços de saúde, locomoção de pacientes, de laboratório, administrativos e outros, nos termos do Decretos nº 4.101 de 18 de março de 2020, e 4.103/20 de 21 de Março de 2020, nos horários de trabalho e jornadas estabelecidas.</p>

        <p><strong>Artigo 2º</strong> – Fica vedada qualquer ausência de servidor lotado na Secretaria de Saúde ao posto e horário normal de trabalho sem autorização expressa da Secretária de Saúde, com a ciência do Comitê Gestor da Crise criado especialmente para esse fim.</p>

        <p><strong>Artigo 3º</strong> – Qualquer coordenador, responsável de setor que venha descumprir o disposto nesta resolução, que referenda os Decretos nº 4.101/20 e 4.103/20 no âmbito da Secretaria Municipal de Saúde, será aplicada as sanções funcionais administrativas nos termos do Estatuto dos Servidores Públicos Municipais de Quatá.</p>

        <p><strong>Artigo 4º</strong> – Esta resolução entra em vigor na data de sus publicação.</p>

        <p><strong>Artigo 5º</strong> – Revogam-se as disposições em contrário.</p>

