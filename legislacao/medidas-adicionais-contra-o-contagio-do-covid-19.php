        <span class='date'>29 de abril de 2020</span>
        <h1>Decreto de medidas adicionais de prevenção de contágio pelo novo Coronavírus ─ DECRETO Nº 4.118</h1>
        <span class='author'>O texto reproduzido tem caráter informativo e não substitui o <a href=""></a>publicado no diário oficial do município</span>

        <p><strong>"DISPÕE SOBRE MEDIDAS ADICIONAIS AOS DECRETOS Nº. 4.101/20 e 4.103/20 COM SUAS ALTERAÇÕES, DE CARÁTER TEMPORÁRIO E EMERGENCIAL, DE PREVENÇÃO DE CONTÁGIO PELO NOVO CORONAVIRUS-COVID-19 E DÁ OUTRAS PROVIDÊNCIAS"</strong></p>

        <p><strong>MARCELO PECCHIO,</strong>, Prefeito Municipal de Quatá, Estado de São Paulo, no uso de suas atribuições legais;</p>

        <p><strong>CONSIDERANDO</strong> as medidas adotadas para enfrentamento de emergência em saúde pública decorrente do COVID-19, conforme estabelecido no Decreto Municipal nº 4.101/20, com suas alterações;</p>

        <p><strong>CONSIDERANDO</strong> a prorrogação da quarentena de isolamento social feita pelo Governo do Estado de São Paulo, através do Decreto nº. 64.946/20 de 17 de abril de 2020, em decorrência da Infecção Humana pelo Novo Coronavírus, reconhecendo como estado de calamidade pública no Estado de São Paulo, que estendeu para o dia 10 de maio de 2020 o prazo para o isolamento social;</p>

        <p><strong>CONSIDERANDO</strong> que a suspensão inicial do atendimento ao público tratadas nos incisos do art. 1º do Decreto nº 4.101, de 18 de março previu, preliminarmente, a suspensão do atendimento ao público do período de 18 à 31 de março apenas;</p>

        <p><strong>CONSIDERANDO</strong> que, após 31 de março, a necessidade da continuidade do isolamento social, que culminou na suspensão do atendimento ao público, revezamento ao trabalho, home office e outros, e que embora o prazo tenha sido prorrogado existe a necessidade de "compensação" das horas ou períodos não laborados, em férias, licença-prêmio, banco de horas ou outra forma (abonadas, dispensa em razão de eleições, etc.);</p>

        <p><strong>CONSIDERANDO</strong> o atendimento das recomendações do Tribunal de Contas do Estado de São Paulo, principalmente a traduzida no Comunicado SDG nº 14/2020, de 03 de abril do corrente exercício, da necessidade de se regulamentar e organizar no âmbito do Município a melhor forma de cumprimento da jornada de trabalho, bem como da efetiva compensação das horas não trabalhadas, em virtude da suspensão dos serviços;</p>

        <p><strong>DECRETA:</strong>

            <p><strong>Artigo 1º</strong> – Fica adotado no âmbito do Município de Quatá, o Decreto Estadual nº 64.946/20 que estende até o dia 10 de maio de 2020 o período de quarentena de que trata o parágrafo único do artigo 1º Decreto Estadual nº 64.881, de 22 de março de 2020, como medida necessária ao enfrentamento da pandemia da COVID-19 (Novo Coronavírus), no Estado de São Paulo.</p>

            <p><strong>Artigo 2º</strong> – Os prazos constantes como no estabelecido nos Decretos Municipais nº 4.101, 4103 e suas alterações, ficam estendidos para o dia 10 de maio de 2020.</p>

            <p><strong>Artigo 3º</strong> – Fica antecipado o recesso escolar que ocorreria no mês de julho para o período compreendido entre 18 de março e 07 de abril de 2020.</p>

            <p><strong>Artigo 4º</strong> – Aqueles servidores públicos cujos locais de trabalho tiveram suas atividades suspensas, no período compreendido entre 18 de março e 30 de abril do corrente ano, deverão realizar as devidas compensações de suas jornadas de trabalho.</p>

            <p><strong>Artigo 5º</strong> – Os Secretários Municipais e dirigentes municipais deverão:</p>

            <p><strong>I</strong> – maximizar, na prestação de serviços à população, o emprego de meios virtuais que dispensem o atendimento presencial;</p>

            <p><strong>II</strong> – determinar a apuração dos dias e horas não laborados pelos servidores por cada pasta administrativa, com o auxílio do Setor de Pessoal;</p>

            <p><strong>III</strong> – realizar o levantamento da existência de férias vencidas, licenças-prêmios, banco de horas, abonadas não usufruídas, folgas por dia de eleição, bem como outros direitos que possam ser utilizados como compensação à jornada de trabalho não realizada.</p>

            <p><strong>Artigo 6º</strong> – Os dias e horas não laborados no período de 18 de março a 30 de abril, deverão, à critério da administração e de acordo firmado entre as partes, serem compensados em férias, licença-prêmio, banco de horas, abonadas, folgas de eleição ou mediante compromisso de compensação futura, em cumprimento ao Comunicado do TCSP - SDG 14/2020, não podendo ficar sem a respectiva compensação.</p>

            <p><strong>§ 1º</strong> – Para fins de compensação, cada Secrataria convocará o servidor público vinculado a sua pasta para fins de formalização de Acordo Individual para Compensação da Jornada de Trabalho, conforme minuta constante do Anexo I do presente Decreto, devendo constar, em comum acordo, a forma de compensação.</p>

            <p><strong>§ 2º</strong> – Os servidores públicos vinculados à Secretaria Municipal de Educação deverão compensar sua jornada de trabalho ou parte dela, através da utilização total dos dias que seriam destinados ao período de recesso escolar antecipado do mês de julho para os meses de março e abril de 2020.</p>

            <p><strong>§ 3º</strong> – o período de compensação poderá ser realizado com a utilização de mais de um benefício do servidor;</p>

            <p><strong>§ 4º</strong> – Para fins de compensação, poderá ocorrer a antecipação de período de férias não vencidas, desde que o período aquisitivo se concretize até o dia 31 de dezembro de 2020;</p>

            <p><strong>§ 5º</strong> – Os servidores públicos que no período previsto no caput deste artigo e que realizaram suas atividades através do teletrabalho (<em>home office</em>), terão suas faltas justificadas através de informação escrita prestada pela respectiva Secretaria Municipal, para fins de registro e arquivamento no prontuário funcional;</p>

            <p><strong>§ 6º</strong> – Os servidores públicos, acima de 60 (sessenta) anos e os que se enquadram no grupo de risco estabelecido pelo Ministério da Saúde, também estarão sujeitos a compensação prevista no presente artigo;</p>

            <p><strong>§ 7º</strong> – Os servidores públicos que não tiverem disponíveis nenhuma das formas de compensação mencionadas, deverão optar pela antecipação de período de férias, limitado ao previsto no § 4º ou mediante compromisso de compensação futura da jornada de trabalho, a ser realizado após o período de restabelecimento do trabalho presencial.</p>

            <p><strong>Artigo 7º</strong> – As férias concedidas durante o período de calamidade pública, em especial as tratadas neste Decreto, o pagamento do 1/3 constitucional correspondente, serão pagos juntamente com o vencimento do mês correspondente.</p>

            <p><strong>§ 1º</strong> ─ Para as atividades em que não seja possível a
                realização do teletrabalho (home office), deverá ser informado por cada uma das
                Secretarias o nome e cargo dos servidores públicos nesta situação, para fins de cadastro
                junto ao Setor de Pessoal de um banco de servidores, os quais, respeitadas as
                peculiaridades de seu cargo, possam ser utilizados em outras Secretarias que estejam
                realizando atividades essenciais, respeitadas as medidas de prevenção ao COVID-19;</p>

            <p><strong>§ 2º</strong> ─ Os servidores públicos que exercerão suas
                atividades através do teletrabalho (home office) poderão utilizar de todos os meios
                existentes para o desenvolvimento do trabalho, inclusive recursos tecnológicos, utilização
                de videoconferência, etc.</p>

            <p><strong>§ 3º</strong> ─ Alternativamente, para os casos em que não
                seja possível a realização do teletrabalho (home office), poderá o servidor público cumprir
                sua jornada mediante a compensação de férias vencidas ou por antecipação, licença-
                prêmio, abonadas ou outra forma de compensação, observadas as normas do presente
                Decreto.</p>

            <p><strong>Artigo 8º</strong> – Os funcionários públicos contratados para as funções de Monitor de Transporte Escolar e Agentes de Apoio Pedagógico contratados temporariamente e vinculados a CLT – Consolidação das Leis do Trabalho, terão seus contratos de trabalho suspensos, nos termos do que dispõe a Medida Provisória nº 927/2020.</p>

            <p><strong>Parágrafo único:</strong> Preferencialmente a suspensão contratual, serão concedidas férias, inclusive àqueles que o período aquisitivo não tenha transcorrido, a partir de 1º de maio de 2020.</p>

            <p><strong>Artigo 9º</strong> – As férias concedidas durante o período de calamidade pública, em especial as tratadas neste Decreto, o pagamento do 1/3 constitucional correspondente, serão pagos juntamente com o vencimento do mês correspondente.</p>

            <p><strong>Artigo 10º</strong> – Fica reestabelecida a obrigatoriedade de marcação do ponto digital para todos os servidores e funcionários públicos que realizam atividades presenciais, como anteriormente a suspensão, ficando dispensado o registro do ponto digital para os que realizarão suas atividades através do teletrabalho (home office), os quais comprovarão seus trabalhos através de critérios a serem adotados por cada Secretaria.</p>

            <p><strong>Parágrafo único:</strong> As Secretarias e/ou Setores que realizarem atividades com a presença de servidores públicos, deverá promover a constante higienização dos equipamentos de ponto, deixando disponível em cada local de ponto álcool em gel 70%.</p>

            <p><strong>Artigo 11º</strong> – Os servidores públicos com aniversário nos meses de julho a dezembro, receberão a primeira parcela do 13º salário até o 5º dia útil do mês de junho de 2020, excepcionalmente.</p>

            <p><strong>Artigo 12º</strong> – Incluem-se nas exceções de que trata o inc. V do art. 1º do Decreto Municipal nº 4.101/20 de 18 de março de 2020, dos serviços que não poderão sofrer a interrupção, os serviços de coleta, separação e acondicionamento de lixo de material reciclável.</p>

            <p><strong>Artigo 13º</strong> – Deverão ser mantidos o regular prosseguimento dos processos licitatórios e processos admissionais, nos termos da legislação vigente.</p>

            <p><strong>Artigo 14º</strong> – Os contratos administrativos de fornecimento de bens de consumo, materiais e serviços que tiveram interrupção do fornecimento ou da prestação dos serviços, deverão ter seus contratos "suspensos", devendo ser feita a anotação em cada pasta contratual através do correspondente termo de aditamento, cientificando os Contratados via Imprensa Oficial do Município.</p>

            <p><strong>Parágrafo único:</strong> Aqueles serviços que necessitam de medição para a comprovação da efetiva prestação de serviços não poderão ser pagos.</p>

            <p><strong>Artigo 15º</strong> – Ficam os Secretários Municipais de Assuntos Jurídicos, Promoção Social, Educação, Saúde, Planejamento, Administração e Diretor de Autarquia, autorizados a expedir normativas internas, visando a regulamentação da prestação de serviços e o atendimento às diretrizes e objetivos do
                presente Decreto, zelando sempre pela legalidade e economicidade.</p>

            <p><strong>Artigo 16º</strong> – Este Decreto entra e vigor na data de sua publicação.</p>

            <p><strong>Artigo 17º</strong> – Revogam-se as disposições em contrário.</p>







