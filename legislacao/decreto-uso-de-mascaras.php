        <span class='date'>05 de maio de 2020</span>
        <h1>Decreto de regulamentação o uso de máscaras ─ DECRETO Nº 4.125</h1>
        <span class='author'>O texto reproduzido tem caráter informativo e não substitui o <a href=""></a>publicado no diário oficial do município</span>

        <p><strong>"DISPÕE SOBRE O USO GERAL E OBRIGATÓRIO DE MÁSCARAS DE PROTEÇÃO FACIAL PELA POPULAÇÃO COMO MEIO COMPLEMENTAR DE PREVENÇÃO AO CORONAVIRUS E SOBRE MEDIDAS ADICIONAIS AOS DECRETOS Nº. 4.101/20 e 4.103/20 COM SUAS ALTERAÇÕES E DÁ OUTRAS PROVIDÊNCIAS"</strong></p>

        <p><strong>MARCELO PECCHIO,</strong>, Prefeito Municipal de Quatá, no uso de suas atribuições legais;</p>

        <p><strong>CONSIDERANDO</strong> as medidas adotadas para enfrentamento de emergência em saúde pública decorrente do COVID-19, conforme estabelecido no Decreto Municipal nº 4.101/20 e 4.103/2020, com suas alterações;</p>

        <p><strong>CONSIDERANDO</strong> a altíssima capacidade de disseminação do vírus agravada pela aglomeração de pessoas em espaços comuns;</p>

        <p><strong>CONSIDERANDO</strong> que os estudos baseados em modelos matemáticos mostram que o isolamento social é a forma mais eficiente para diminuir a contaminação em massa da população;</p>

        <p><strong>CONSIDERANDO</strong> a edição do Decreto Estadual nº 64.959, de 04 de maio de 2020, que dispõe sobre o uso geral e obrigatório de máscaras de proteção facial, no contexto da pandemia da COVID-19, como medida adicional ao distanciamento social, a partir de 07 de maio de 2020</p>

        <p><strong>CONSIDERANDO</strong> a necessidade de se alinhar as determinações do Governo do Estado de São Paulo com as determinações do Poder Público Municipal</p>

        <p><strong>DECRETA:</strong>


            <p><strong>Artigo 1º</strong> ─ Sem prejuízo de todas as recomendações preventivas e de isolamento social das autoridades públicas, fica obrigatória, a partir de 07 de maio de 2020, a toda população quando for necessário sair de casa, a utilização de máscaras de proteção facial, sendo essas de uso profissional ou não.</p>

            <p><strong>Parágrafo único</strong> – Para fins deste Decreto, as máscaras de proteção facial não profissionais podem ser de fabricação artesanal/caseira, desde que produzidas segundo as orientações do Ministério da Saúde, de acordo com a Nota Informativa nº. 3/2020-CGGAP/DESF/SAPS/MS.</p>

            <p><strong>Artigo 2º</strong> ─ O descumprimento da medida prevista no artigo anterior sujeitará o infrator as seguintes penalidades:</p>

            <p><strong>I</strong> ─ recomendação verbal;</p>
            <p><strong>II</strong> ─ advertência;</p>
            <p><strong>III</strong> ─ multa no valor de R$ 100,00 (cem reais), para primeira incidência;</p>
            <p><strong>IV</strong> ─ multa no valor de R$ 200,00 (duzentos reais) para cada reincidência;</p>
            <p><strong>V</strong> ─ demais sanções previstas nas esferas administrativa, cível e penal.</p>

            <p><strong>Parágrafo único</strong> – A multa prevista no inciso III, do caput deste artigo poderá ser aplicada de imediato em caso de não atendimento pelo infrator da recomendação e da advertência feita pelo agente público municipal. </p>

            <p><strong>Artigo 3º</strong> ─ As empresas e/ou estabelecimentos
                localizados no Município de Quatá, de acordo com os Decretos Municipais anteriores,
                como condição de funcionamento e atendimento, deverão exigir de todos, inclusive de
                seus empregados, colaboradores e clientes o uso obrigatório de máscara facial,
                profissional ou não.

                <p><strong>§ 1º</strong> ─ Compete ao responsável pelo estabelecimento a fiscalização quanto ao uso obrigatório de máscaras nas dependências internas. </p>

                <p><strong>§ 2º</strong> ─ Compete ao responsável pelo estabelecimento adotar as medidas para sinalizar e informar aos munícipes sobre a impossibilidade de entrada e permanência nos locais sem o uso de máscaras de proteção facial.</p>

                <p><strong>§ 3º</strong> ─ O descumprimento do disposto neste artigo implicará nas sanções previstas no caput, do artigo 7º, do Decreto Municipal nº 4.107/20, sem prejuízo de responsabilização nas esferas cível e criminal.</p>

                <p><strong>Artigo 4º</strong> ─ O uso de máscaras é igualmente obrigatório para os servidores públicos municipais em seus setores de trabalho, tanto para os que estejam atendendo ao público quanto para os que estejam exercendo atividades internas.</p>

                <p><strong>§ 1º</strong> ─ É obrigatório o uso de máscara facial pelos participantes e público nas sessões de licitação, além de obedecido o distanciamento de pelo menos 1 (um) metro entre as pessoas e a disponibilização de álcool gel para o público e participantes presentes.</p>

                <p><strong>§ 2º</strong> ─ É de responsabilidade dos Secretários Municipais a fiscalização do uso de máscara pelos servidores da sua pasta.</p>

                <p><strong>§ 3º – Fica determinado aos servidores das portarias e da segurança em geral das repartições públicas, impedir o ingresso de servidores e munícipes nas dependências, sem o uso de máscara facial.</p>

                <p><strong>§ 4º</strong> ─ O servidor que concorrer para o descumprimento do disposto no caput deste artigo, ficará sujeito à responsabilidade administrativa disciplinar.</p>

                <p><strong>Artigo 5º</strong> ─ O § 2º, do artigo 3º, do Decreto nº 4.103/20, alterado pelo Decreto nº 4.114/20, passa a vigorar com a seguinte redação:</p>

                <em>
                    <p>"§ 2º – Fica permitido o funcionamento do comércio de cunho essencial, como farmácias, mercados, mercearias, supermercados, açougues, padarias e quitandas, ficando recomendado que atenda as recomendações de higienização das mãos com álcool gel, tanto de seus funcionários como dos clientes, evite a aglomeração de pessoas, controlando o número de pessoas no interior do estabelecimento e adote as seguintes medidas:</p>

                    <p>I ─ higienização de carrinhos e/ou cestos de compra;</p>

                    <p>II ─ manter distanciamento de dois metros entre as pessoas em fila de espera;</p>

                    <p>III ─ intensificar a higienização do interior e exterior do estabelecimento, principalmente balcões e acesso dos caixas de recebimento;</p>

                    <p>IV ─ providenciar para que o manuseio de cédulas de dinheiro seja feito com a utilização de luvas;</p>

                    <p>V ─ providenciar para que todas as máquinas de cartões de crédito e débito sejam higienizadas a cada utilização;</p>

                    <p>VI ─ limitar/controlar a entrada no estabelecimento para somente 01 (uma) pessoa por família, não sendo permitida a entrada de crianças;</p>

                    <p>VII ─ determinar uso obrigatório de máscaras aos funcionários do estabelecimento;</p>

                    <p>VIII ─ não permitir a entrada no estabelecimento de pessoas sem a utilização de máscaras;</p>

                    <p>IV ─ uso obrigatório de luva e touca pelos funcionários que manuseiam alimentos nos setores de frios, carnes, frutas e legumes."</p>
                </em>

                <p><strong>Artigo 6º</strong> ─ O § 4º, do artigo 3º, do Decreto nº 4.114/20, passa a vigorar com a seguinte redação: </p>

                <em>
                    <p>"§ 4º – Ficam excetuadas da suspensão determinada por este Decreto, o funcionamento das instituições ou agências bancárias, postos de correspondentes bancários e as agências dos correios, por serem consideradas serviço essencial à população e deverão adotar as seguintes providências:</p>

                    <p>I ─ no atendimento interno deverá ser limitado o número de pessoas e respeitada a distância mínima de 02 (dois) metros entre os pontos de atendimento;</p>

                    <p>II ─ seja dada preferência ao atendimento nos caixas eletrônicos das agências;</p>

                    <p>III ─ a fila das pessoas que aguardam atendimento deverá ser organizada no lado externo das agências, guardando ─se a distância de pelo menos 01 (um) metro entre as pessoas;</p>

                    <p>IV ─ exigência do uso de máscaras pelo funcionários do estabelecimento;</p>

                    <p>V ─ não permitir a entrada de pessoas sem a utilização de máscaras.</p>

                    <p>VI ─ higienização constante dos caixas eletrônicos."</p>
                </em>

                <p><strong>Artigo 7º</strong> ─ Este Decreto entra em vigor a partir de 07 de maio de 2020.</p>

                <p><strong>Artigo 8º</strong> ─ Revogam-se as disposições em contrário.</p>

