        <span class='date'>21 de março de 2020</span>
        <h1>Decreto de estado de calamidade pública ─ DECRETO Nº 4.103</h1>
        <span class='author'>O texto reproduzido tem caráter informativo e não substitui o <a href=""></a>publicado
            no
            diário oficial do município</span>

        <p><strong>"DECRETA ESTADO DE CALAMIDADE PÚBLICA NO MUNICÍPIO DE QUATÁ COMO MEDIDA DE ENFRENTAMENTO DA
                EMERGÊNCIA DE SAÚDE PÚBLICA DECORRENTE DA PANDEMIA DO COVID-19 (NOVO CORONAVIRUS) E DISPÕE SOBRE A
                ADOÇÃO DE MEDIDAS COMPLEMENTARES NO ÂMBITO DA ADMINISTRAÇÃO PÚBLICA E SETOR PRIVADO
                MUNICIPAL"</strong></p>

        <p><strong>CONSIDERANDO</strong> as notícias mais recentes a respeito da elevada capacidade de difusão do
            COVID-19 (Novo Coronavírus), nos termos declarados pela Organização Mundial da Saúde;</p>

        <p><strong>CONSIDERANDO</strong> as informações da alta escalabilidade viral do COVID-19 e os elevados
            riscos a saúde pública;</p>

        <p><strong>CONSIDERANDO</strong> os últimos Decretos do Governador do Estado, bem como seu pronunciamento
            feito
            na data de 20/03/2020 com a decretação de estado de calamidade pública no Estado de São Paulo à partir
            do dia 21 de março de 2020;</p>

        <p><strong>CONSIDERANDO</strong> as Recomendações Administrativas feitas pelo Ministério Público do Estado
            de São Paulo, através da Promotoria de Justiça de Quatá;</p>

        <p><strong>DECRETA:</strong>

            <p><strong>Artigo 1º</strong> – Fica decretado ESTADO DE CALAMIDADE PÚBLICA no Município de Quatá em
                razão da pandemia do
                COVID-19 (NOVO CORONAVIRUS).</p>

            <p><strong>Artigo 2º</strong> – No âmbito municipal, fica determinada a suspensão, pelo prazo de 10
                (dez) dias, à partir de 23/03/2020, das atividades e serviços públicos não essenciais, que não
                puderem ser realizados por meio digital ou mediante trabalho remoto, com a dispensa dos servidores
                públicos.</p>

            <p><strong>Artigo 3º</strong> – Em virtude da aglomeração ou circulação de pessoas, no âmbito dos outros
                poderes, órgãos e
                entidades autônomas, bem como no setor privado do Município, fica determinada a suspensão, pelo
                prazo de 10 (dez) dias, à partir de 23/03/2020, de todas as atividades e serviços não essenciais,
                como academias, clubes, associações recreativas, lanchonetes, bares, conveniência, restaurantes e
                comércio em geral (lojas e afins), bem como missas, cultos e atividades religiosas, reuniões ou
                eventos com público de qualquer natureza.</p>

            <p><strong>§ 1º</strong> – Fica autorizado o funcionamento de bares, lanchonetes, restaurantes e demais
                estabelecimentos de gêneros alimentícios, exclusivamente para atendimento de entrega a domicílio
                (delivery).</p>

            <p><strong>§ 2º</strong> – Fica permitido o funcionamento do comércio de cunho essencial, como
                farmácias, supermercados, açougues, padarias, quitandas e postos de gasolina, ficando estabelecido o
                limite de 08 (oito) horas diárias e recomendado que atenda as recomendações de higienização das mãos
                com álcool gel, tanto de seus funcionários como dos clientes e evite a aglomeração de pessoas.</p>

            <p><strong>§ 3º</strong> – Ficam excetuadas da suspensão determinada por este Decreto, o funcionamento
                das instituições ou agências bancárias, por ser considerado serviço essencial à população e deverão
                adotar as seguintes providências:</p>

            <p>I- no atendimento interno deverá ser limitado o número de pessoas e respeitada a distância mínima de
                02 (dois) metros entre os pontos de atendimento;</p>

            <p>II- seja dada preferência ao atendimento nos caixas eletrônicos das agências;</p>

            <p>III- a fila das pessoas que aguardam atendimento deverá ser organizada no lado externo das agências,
                guardando-se a distância de pelo menos 01 (um) metro entre as pessoas.</p>

            <p><strong>Artigo 4º</strong> – Fica autorizado o funcionamento das atividades essenciais como:</p>

            <p>I- serviços de saúde, assistência médica e hospitalar;</p>

            <p>II – clínicas de fisioterapia, consultórios médicos e odontológicos, clínicas de psicologia,
                laboratórios de análises clínicas e farmacêuticas, limitando o atendimento para os casos
                extremamente necessários;</p>

            <p>III – estabelecimentos comerciais de venda de água e gás, preferencialmente para vendas com entrega a
                domicílio (delivery);</p>

            <p>IV- agências de concessionária de energia e água, para atendimentos de urgências;</p>

            <p>V- serviços funerários, com orientações específicas para que os velórios funcionem com capacidade
                reduzida de pessoas;</p>

            <p>VI- clínicas veterinárias e venda de suprimentos animais;</p>

            <p>Parágrafo único – os locais e estabelecimentos mencionados neste artigo deverão adotar as seguintes
                medidas de segurança:</p>

            <p>I- disponibilizar álcool gel na entrada dos estabelecimentos para utilização dos funcionários e
                clientes;</p>

            <p>II- manter o local de circulação de pessoas arejado;</p>

            <p>III- limitar o número de pessoas e evitar aglomeração.</p>

            <p><strong>Artigo 5º</strong> – Os servidores públicos municipais integrantes de grupos de risco, mesmo
                que lotados em serviços essenciais, deverão ser imediatamente dispensados do trabalho.</p>

            <p><strong>Artigo 6º</strong> – A Secretaria de Promoção Social, por meio de sua estrutura e com o apoio
                dos demais órgãos competentes, deverá reorganizar as atividades assistenciais de forma a minimizar o
                impacto às pessoas em situação de vulnerabilidade social.</p>

            <p><strong>Artigo 7º</strong> – Ficam os órgãos e autoridades municipais autorizados e obrigados a dar
                cumprimento a todas as disposições deste Decreto, bem como às demais legislações relacionadas à
                pandemia do COVID-19 agindo imediatamente, nos casos de descumprimento, se necessário com auxílio
                das forças de segurança pública, na emissão de atos necessários enquanto perdurar a vigência deste
                Decreto.</p>

            <p><strong>Artigo 8º</strong> – As pessoas, físicas ou jurídicas, deverão sujeitar-se ao cumprimento das
                medidas previstas neste Decreto, sendo que seu descumprimento acarretará responsabilização nos
                termos previstos da lei, tanto na esfera cível como criminal e administrativa.</p>

            <p><strong>Artigo 9º</strong> – Fica recomendado a toda população que, se possível, permaneçam em suas
                casas e, nos casos de
                deslocamentos necessários ou urgentes, sigam as orientações de forma a evitar aglomerações e contato
                próximo com as demais pessoas, fazer as compras necessárias de maneira rápida, evitar a exposição de
                idosos, crianças e pessoas consideradas de grupos de risco e, se possível, realizar as compras em
                favor de vizinhos, familiares ou amigos evitando, assim, a exposição de mais pessoas na rua.</p>

            <p><strong>Artigo 10</strong> – As medidas previstas neste Decreto poderão ser reavaliadas a qualquer
                momento.</p>

            <p><strong>Artigo 11</strong> – Ficam mantidas, no que couber e não conflitar com o presente Decreto, as
                medidas descritas no Decreto nº. 4.101 de 18 de março de 2020.</p>

            <p><strong>Artigo 12</strong> – Este Decreto entra em vigor na data de sua publicação.</p>

