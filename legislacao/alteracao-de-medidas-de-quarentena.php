		<h1>Decreto de Quarentena e Medidas Complementares de Prevenção à Pandemia do COVID-19, altera Decreto 4.103 ─ DECRETO Nº 4.107</h1>
		<span class='author'>O texto reproduzido tem caráter informativo e não substitui o <a href=""></a>publicado no diário oficial do município</span>

		<p><strong>"ALTERA O DECRETO MUNICIPAL Nº. 4.103 DE 21/03/2020, DECRETA QUARENTENA NO MUNICÍPIO DE QUATÁ COMO MEDIDA DE ENFRENTAMENTO DA EMERGÊNCIA DE SAÚDE PÚBLICA DECORRENTE DA PANDEMIA DO COVID-19 (NOVO CORONAVIRUS) E DISPÕE SOBRE A ADOÇÃO DE MEDIDAS COMPLEMENTARES NO ÂMBITO DA ADMINISTRAÇÃO PÚBLICA E SETOR PRIVADO MUNICIPAL".</strong></p>

        <p><strong>MARCELO DE SOUZA PECCHIO</strong>, Prefeito do Município de Quatá, Estado de São Paulo, no uso e suas atribuições legais;</p>

        <p><strong>CONSIDERANDO</strong> o disposto no Decreto Municipal nº 4.103, de 21 de março de 2020, que decretou estado de calamidade pública no Município de Quatá;</p>

        <p><strong>CONSIDERANDO</strong> que após a edição do mencionado Decreto Municipal, o Governo do Estado de São Paulo publicou o Decreto Estadual nº 64.881, de 22 de março e 2020, que decretou quarentena no Estado de São Paulo, no contexto da pandemia do COVID-19 (Novo Coronavírus);</p>
 
        <p><strong>CONSIDERANDO</strong> a necessidade de se alinhar as determinações do Governo do Estado de São Paulo com as determinações do Poder Público Municipal;</p>

        <p><strong>DECRETA:</strong></p>

        <p><strong>Artigo 1º</strong> – Ficam alterados os prazos constantes dos artigos 2º e 3º do Decreto Municipal nº 4.103, de 21 de março de 2020, passando os mesmos a vigorarem de 23/03/2020 a 07/04/2020, período estabelecido como medida de quarentena, podendo este prazo ser prorrogado.</p>

        <p><strong>Artigo 2º</strong> – Fica recomendado que a circulação de pessoas no âmbito do Município de Quatá, Estado de São Paulo se limite às necessidades imediatas de alimentação, cuidados de saúde e exercícios de atividades essenciais.</p>

        <p><strong>Artigo 3º</strong> – O artigo 3º do Decreto nº. 4.103 de 21 de março de 2020, passa a vigorar com a seguinte redação:</p>

        <em>"<strong>Artigo 3º</strong> – Em virtude da aglomeração ou circulação de pessoas, no âmbito dos outros poderes, órgãos e entidades autônomas, bem como no setor privado do Município, fica determinada a suspensão, no período de 23/03/2020 A 07/04/2020, de todas as atividades e serviços não essenciais, como academias, clubes, associações recreativas, bem como a realização de missas, cultos e atividades religiosas, reuniões ou eventos com público de qualquer natureza.

        <p><strong>§ 1º</strong> – Fica autorizado o funcionamento do comércio em geral (lojas e afins),incluindo-se bares, lanchonetes, restaurantes, conveniências e demais estabelecimentos de gêneros alimentícios, exclusivamente para atendimento de serviços com entrega a domicílio (<em>delivery</em>), sendo que os estabelecimentos comerciais deverão manter fechadas as portas de acesso do público ao seu interior.</p>

        <p><strong>§ 2º</strong> – Fica permitido o funcionamento do comércio de cunho essencial, como farmácias, mercados, supermercados, açougues, padarias e quitandas, ficando estabelecido o limite de 08 (oito) horas diárias e recomendado que atenda as recomendações de higienização das mãos com álcool gel, tanto de seus funcionários como dos clientes e evite a aglomeração de pessoas.</p>

        <p><strong>§ 3º</strong> – Fica permitido o funcionamento dos postos de gasolina, nos moldes da determinação expressa no artigo 5º da Resolução da ANP nº. 812, de 23 de março de 2020, ressaltando que as conveniências deverão estar fechadas para o público, somente com a opção de entrega de produtos a domicílio (<em>delivery)</em>.</p>

        <p>
        <strong>§ 4º</strong> – Ficam excetuadas da suspensão determinada por este Decreto, o funcionamento das instituições ou agências bancárias e as agências dos correios (não abrangendo aqui os postos de correspondentes bancários), por serem consideradas serviço essencial à população e deverão adotar as seguintes providências:"</p></em>

        <p>I- no atendimento interno deverá ser limitado o número de pessoas e respeitada a distância mínima de 02 (dois) metros entre os pontos de atendimento;</p>

        <p>II- seja dada preferência ao atendimento nos caixas eletrônicos das agências;</p>
        
        <p>III- a fila das pessoas que aguardam atendimento deverá ser organizada no lado externo das agências, guardando-se a distância de pelo menos 01 (um) metro entre as pessoas.</p>

        <em><p>"VII- lojas de materiais de construção, exclusivamente com atendimento por telefone e entregas a domicílio (delivery);</p>

        <p>VIII- as oficinas mecânicas e borracharias sendo que, a solicitação dos serviços, deverão ser feitas por telefone para atendimentos dos casos de urgência."</p></em>

        <p><strong>Artigo 5º</strong> – Ficam suspensos os prazos regulamentares nos processos administrativos disciplinares, pelo prazo de 30 (trinta) dias, sem prejuízo de eventual prorrogação.</p>

        <p><strong>Artigo 6º</strong> – Haverá remanejamento de servidores públicos afins, na medida da necessidade dos serviços públicos, podendo haver prestação de serviços em outros setores.</p>

        <p><strong>Artigo 7º</strong> – O descumprimento do presente Decreto, bem como do Decreto nº. 4.103, de 21 de março de 2020, sujeitará o infrator a aplicação das penalidades administrativas na legislação municipal, sem prejuízo da aplicação de multa, no valor correspondente a 10 UFM’s (Unidade Fiscal do Município), interdição total ou parcial da atividade, cassação de alvará de localização e funcionamento, penalidades civis e penais, notadamente ao disposto nos artigos 268 e 330, ambos do Código Penal, se a infração não constituir crime mais grave.</p>

        <p><strong>Parágrafo único</strong> – A fiscalização do presente Decreto e das demais normatizações anteriores previstas, ficará a cargo dos órgãos competentes municipais de fiscalização e de vigilância sanitária, sem prejuízo da atuação dos órgãos de segurança pública.</p>

        <p><strong>Artigo 8º</strong> – O Comitê Gestor de Crise-CGC, criado pelo Decreto nº. 4.101, de 18 de março de 2020 e instituído pela Portaria nº. 36.931/2020, deliberará sobre casos adicionais abrangidos pela medida de quarentena de que trata este Decreto, mediante resoluções.</p>

        <p><strong>Artigo 9º</strong> – Ficam ratificados os demais termos do Decreto Municipal nº 4.103, de 21 de março de 2020, sendo que, as medidas previstas poderão ser reavaliadas a qualquer momento.</p>

        <p><strong>Artigo 10º</strong> – Este Decreto entra em vigor na data de sua publicação.</p>
