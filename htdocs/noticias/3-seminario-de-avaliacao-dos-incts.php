        <p>
        Os Seminários de Avaliação dos Institutos Nacionais de Ciência e Tecnologia (INCT) têm o objetivo de avaliar a execução dos projetos e de seus respectivos resultados, permitindo conhecer e divulgar os conhecimentos, as tecnologias e as inovações decorrentes do desenvolvimento dos projetos que o o Conselho Nacional de Desenvolvimento Científico e Tecnológico (CNPq) financia no âmbito do Programa Institutos Nacionais de Ciência e Tecnologia.
        </p>

        <p>O 3º Seminário de Avaliação dos INCT é uma avaliação de meio-termo, que ocorre durante a execução dos projetos. É um evento estratégico para o Programa INCT, uma vez que se conStitui em uma oportunidade de se identificar os avanços alcançados pelos INCT, além de permitir que se visualizem oportunidades de aprimoramento e de eventuais ajustes de metas e/ou cronogramas.</p>

        <p>
        O objetivo deste 3º Seminário é avaliar o andamento da execução dos 105 (cento e cinco) INCT vigentes, sendo 3 (três) INCTs selecionados pelo Edital 71/2010, que totalizou investimentos da ordem de R$ 39 milhões, e 102 (cento e dois) selecionados pelo Edital 16/2014, num investimento total de R$ 660 milhões.
        </p>

        <p>
        A avaliação dos projetos é de responsabilidade do CNPq e o Centro de Gestão e Estudos Estratégicos (CGEE) tem trabalhado de forma articulada com o Conselho para a avaliação do Programa como um todo.
        </p>

        <p>
        O CNPq, em atendimento aos pilares centrais do Programa, introduziu na programação desta edição do Seminário de Avaliação, além do desenvolvimento das atividades avaliativas, atividades de divulgação e popularização da ciência, tecnologia e inovação, bem como outras que visam possibilitar um espaço de interação entre os INCT e os setores público e privado.
        </p>   

        <p>O seminário de avaliação ocorrerá em Brasília no período de 19 a 21 novembro de 2019. Para se inscrever ou obter mais informações acesse: <a target='_blank' href="https://seminarioinct.cgee.org.br/">seminarioinct.cgee.org.br/</a>.</p>


